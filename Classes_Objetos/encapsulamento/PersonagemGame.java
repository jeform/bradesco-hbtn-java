public class PersonagemGame {
    private int saudeAtual;
    private String nome;
    
    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getNome() {
        return nome;
    }
    
    public void saudeAtual(int saude) {
        this.saudeAtual = saude;
    }

    public int getSaudeAtual() {
        return saudeAtual;
    }
}